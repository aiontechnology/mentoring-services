package io.aiontechnology.mentoring.entity.reference;

import jakarta.persistence.Column;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.Id;
import jakarta.persistence.MappedSuperclass;
import lombok.Getter;
import lombok.Setter;
import org.hibernate.annotations.GenericGenerator;

import java.util.UUID;

/**
 * Abstract type that is the base of reference entities.
 *
 * @author Whitney Hunter
 * @since 0.3.0
 */
@MappedSuperclass
@Setter
@Getter
public abstract class AbstractReference implements Nameable {

    /** This ID of the reference value. */
    @Id
    @GeneratedValue(generator = "UUID")
    @GenericGenerator(name = "UUID", strategy = "org.hibernate.id.UUIDGenerator")
    private UUID id;

    /** The name of the reference value. */
    @Column
    private String name;

}
